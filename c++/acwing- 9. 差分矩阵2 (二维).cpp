// 差分矩阵2 (二维)

// 作用：可以使矩阵的某一个子矩阵范围都加上一个数
// ps : 平时使用的时候只需要考虑如何去更新的可以了

// AcWing. 798 差分矩阵

// 题目描述：
// 输入一个n行m列的整数矩阵，再输入q个操作，每个操作包含五个整数x1, y1, x2, y2, c，
// 其中(x1, y1)和(x2, y2)表示一个子矩阵的左上角坐标和右下角坐标。
// 每个操作都要将选中的子矩阵中的每个元素的值加上c。
// 请你将进行完所有操作后的矩阵输出。

// 输入格式
// 第一行包含整数n,m,q。
// 接下来n行，每行包含m个整数，表示整数矩阵。
// 接下来q行，每行包含5个整数x1, y1, x2, y2, c，表示一个操作。

// 输出格式
// 共 n 行，每行 m 个整数，表示所有操作进行完毕后的最终矩阵。

// 数据范围
// 1 ≤ n, m ≤ 1000
// 1 ≤ q ≤ 100000
// 1 ≤ x1 ≤ x2 ≤ n
// 1 ≤ y1 ≤ y2 ≤ m
// -1000 ≤ c ≤ 1000
// -1000 ≤ 矩阵内元素的值 ≤ 1000

// 输入样例：
// 3 4 3
// 1 2 2 1
// 3 2 2 1
// 1 1 1 1
// 1 1 2 2 1
// 1 3 2 3 2
// 3 1 3 4 1
// 输出样例：
// 2 3 4 1
// 4 3 4 1
// 2 2 2 2

#include <iostream>
using namespace std;

// 定义数组的最大长度，这里假设矩阵的行数和列数最大为 1010
const int N = 1010;

// n 表示矩阵的行数，m 表示矩阵的列数，q 表示操作的次数
int n, m, q;
// a 数组用于存储原始的二维矩阵
// b 数组用于存储 a 数组的二维差分数组
int a[N][N], b[N][N];

// 插入函数，用于对二维差分数组进行修改
// 这个函数的作用是将原矩阵中以 (x1, y1) 为左上角，(x2, y2) 为右下角的子矩阵中的每个元素都加上 c
// 原理是通过修改差分数组的四个关键位置的值来实现对原矩阵指定子矩阵的修改
void insert(int x1, int y1, int x2, int y2, int c)
{
    // 将差分数组中 (x1, y1) 位置的元素加上 c
    // 这一步意味着从原矩阵的 (x1, y1) 位置开始，后面的元素都会受到加上 c 的影响
    b[x1][y1] += c;
    // 将差分数组中 (x2 + 1, y1) 位置的元素减去 c
    // 这一步是为了限制加上 c 的影响范围，使得从原矩阵的第 x2 + 1 行开始，第 y1 列及以后的元素不受加上 c 的影响
    b[x2 + 1][y1] -= c;
    // 将差分数组中 (x1, y2 + 1) 位置的元素减去 c
    // 这一步是为了限制加上 c 的影响范围，使得从原矩阵的第 x1 行开始，第 y2 + 1 列及以后的元素不受加上 c 的影响
    b[x1][y2 + 1] -= c;
    // 将差分数组中 (x2 + 1, y2 + 1) 位置的元素加上 c
    // 这一步是为了修正前面两次减法多减去的部分，确保影响范围正确
    b[x2 + 1][y2 + 1] += c;
}

int main()
{
    // 从标准输入读取矩阵的行数 n、列数 m 和操作的次数 q
    scanf("%d%d%d", &n, &m, &q);

    // 输入原始的二维矩阵，存储到 a 数组中
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            // 从标准输入读取矩阵中每个元素的值
            scanf("%d", &a[i][j]);
        }
    }

    // 得到原矩阵的二维差分数组
    // 对于原矩阵的每个元素 a[i][j]，可以看作是在以 (i, j) 为左上角和右下角的子矩阵上插入 a[i][j]
    // 这样就可以通过 insert 函数来构建二维差分数组
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            insert(i, j, i, j, a[i][j]);
        }
    }

    // 循环 q 次，处理 q 个操作
    while (q--)
    {
        // 定义五个变量，用于存储每次操作的左上角坐标 (x1, y1)、右下角坐标 (x2, y2) 和要加上的值 c
        int x1, y1, x2, y2, c;
        // 从标准输入读取每次操作的具体参数
        cin >> x1 >> y1 >> x2 >> y2 >> c;
        // 调用 insert 函数，对二维差分数组进行修改，从而间接修改原矩阵
        insert(x1, y1, x2, y2, c);
    }

    // 对二维差分数组求前缀和，得到经过修改后的原矩阵
    // 因为差分和前缀和是互逆的操作，对二维差分数组求前缀和就可以得到经过修改后的原矩阵
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            // 通过容斥原理计算前缀和
            // b[i - 1][j] 表示上方子矩阵的和
            // b[i][j - 1] 表示左方子矩阵的和
            // b[i - 1][j - 1] 表示左上角子矩阵的和，由于前面两个和都包含了它，所以要减去一次
            b[i][j] += b[i - 1][j] + b[i][j - 1] - b[i - 1][j - 1];
        }
    }

    // 打印输出经过所有操作后的矩阵
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            // 依次输出矩阵中每个元素的值，元素之间用空格分隔
            printf("%d ", b[i][j]);
        }
        // 每输出完一行元素，换行
        puts("");
    }

    return 0;
}