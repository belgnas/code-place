#include<iostream>
using namespace std;
int main() {
    
    char c;
    cin >> c;
    if (c == 'A') {
        cout << "red" << endl;
    }
    else if (c == 'B') {
        cout << "orange" << endl;
    }
    else if (c == 'C') {
        cout << "blue" << endl;
    }
    else if (c == 'D') {
        cout << "green" << endl;
    }
    
    return 0;
}