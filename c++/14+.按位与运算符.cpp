// 位运算（Bitwise operation）是直接对整数在内存中的二进制位进行操作的运算，它的运算效率通常比较高，在很多底层开发、算法优化以及对空间、时间要求苛刻的场景中有着广泛应用。以下是常见的几种位运算及其介绍：

// 1. 按位与（&）
// 运算规则：
// 将参与运算的两个数对应的二进制位进行 “与” 操作。只有当对应的两个二进制位都为 1 时，结果位才为 1，否则为 0。例如，对于二进制数 0101（十进制为 5）和 0110（十进制为 6）进行按位与运算：
//   0101
// & 0110
// ------
//   0100 （十进制为4）
// 即 5 & 6 = 4。
// 应用场景：
// 常用于判断一个数的某些特定二进制位是否为 1，比如判断一个整数是否为偶数（偶数的二进制表示中最低位为 0，可通过 num & 1 判断，如果结果为 0 则是偶数，因为 1 的二进制是 0001，按位与后最低位为 0 才符合条件）；还可以用于权限管理系统中，通过不同二进制位表示不同权限，按位与来判断用户是否具有某些权限等场景。

// 2. 按位或（|）
// 运算规则：
// 把参与运算的两个数对应的二进制位进行 “或” 操作。只要对应的两个二进制位中有一个为 1，结果位就为 1；只有当两个二进制位都为 0 时，结果位才为 0。例如，对于二进制数 0101（十进制为 5）和 0110（十进制为 6）进行按位或运算：
//   0101
// | 0110
// ------
//   0111 （十进制为7）
// 即 5 | 6 = 7。
// 应用场景：
// 在设置某些标志位的时候经常会用到，例如在一个系统中用一个整数的不同二进制位来表示不同的状态（如第 0 位表示是否开启功能 A，第 1 位表示是否开启功能 B 等），要开启多个功能时，就可以通过按位或操作来设置相应的位为 1，达到开启多个状态的目的。

// 3. 按位异或（^）
// 运算规则：
// 对参与运算的两个数对应的二进制位进行 “异或” 操作。当对应的两个二进制位不同（一个为 0，一个为 1）时，结果位为 1；当两个二进制位相同时，结果位为 0。例如，对于二进制数 0101（十进制为 5）和 0110（十进制为 6）进行按位异或运算：
//   0101
// ^ 0110
// ------
//   0011 （十进制为3）
// 即 5 ^ 6 = 3。
// 应用场景：
// 常用于数据加密、交换两个变量的值（不需要借助额外的临时变量，例如 a = a ^ b; b = a ^ b; a = a ^ b; 就可以实现 a 和 b 的值交换）、检测数据在传输过程中是否发生变化（原始数据和传输后的数据进行异或，如果结果为 0 则表示数据未改变）等情况。

// 4. 按位取反（~）
// 运算规则：
// 对一个数的二进制表示的每一位进行取反操作，即 0 变成 1，1 变成 0。需要注意的是，按位取反操作符是单目运算符，只作用于一个操作数。例如，对于二进制数 0101（十进制为 5）进行按位取反：
// ~ 0101
// ------
//   1010 （十进制为 -6，在有符号数表示下，涉及到补码等知识，取反后的值需要按照补码规则转换为十进制）
// 应用场景：
// 在一些底层的硬件操作或者需要对数据的二进制表示进行反转的场景中会用到，不过使用相对较少，因为它改变了整个数的所有二进制位，使用时要特别注意对有符号数带来的数值变化以及结果的理解。

// 5. 左移（<<）
// 运算规则：
// 将一个数的二进制表示向左移动指定的位数，右边空出的位用 0 填充。左移 n 位相当于乘以 2 的 n 次方。例如，对于二进制数 0010（十进制为 2）左移 2 位：
//   0010 << 2
// ------
//   1000 （十进制为8，相当于2 * 2? = 8）
// 应用场景：
// 常用于快速进行乘法运算（尤其是乘方为 2 的幂次的情况），在一些底层的内存地址计算、位掩码操作等场景中也比较常用，能够高效地对二进制数据进行位级别的调整。

// 6. 右移（>>）
// 运算规则：
// 把一个数的二进制表示向右移动指定的位数。对于无符号数，左边空出的位用 0 填充；对于有符号数，在大多数编程语言中（采用算术右移规则），左边空出的位用原来的符号位（最高位）填充，右移 n 位相当于除以 2 的 n 次方（取整）。例如，对于二进制数 1010（十进制为 -6，以有符号数的补码形式表示）右移 2 位：
//   1010 >> 2
// ------
//   1110 （十进制为 -2，按照算术右移规则，左边用符号位1填充）
// 应用场景：
// 常用于快速进行除法运算（尤其是除数为 2 的幂次的情况），在一些数据压缩、位字段提取等场景中也有应用，能帮助高效地对数据的二进制表示进行缩小或者提取特定部分等操作。