// 类和对象-多态-多态的基本概念

//  多态
// 多态是C++面向对象的三大特性之一

// 多态分为两类
//  1.静态多态：函数重载 和 运算符重载 属于静态多态，复用函数名
//  2.动态多态：派生类和虚函数实现运行时多态

// 静态多态和动态多态的区别
//  1.静态多态的函数地址早绑定 - 编译阶段确定函数地址
//  2.动态多态的函数地址晚绑定 - 运行阶段确定函数地址

// 下面通过案例进行讲解多态

#include<iostream>
using namespace std;

// 多态

// 动物类
class Animal
{
public:
    // 虚函数
    // 虚函数是实现动态多态的核心机制
    // speak函数就是虚函数
    // 函数前面加上 virtual关键字，变成虚函数，那么编译器在编译的时候就不能确定函数的调用了
    virtual void speak()
    {
        cout << "动物在说话" << endl;
    }
};

// 猫类
class Cat : public Animal
{
public:
    // 当在基类中把一个函数声明为虚函数后，派生类可以重写这个函数，并且通过基类指针或引用调用该函数时，编译器会在运行阶段根据对象的实际类型来决定调用哪个类的函数版本。
    // 个人理解：Cat类可以覆盖掉Animal类的虚函数
    void speak()
    {
        cout << "小猫在说话" << endl;
    }
};

// 狗类
class Dog : public Animal
{
public:
    // 子类的virtual想写就写，没有影响（反正写了它自己也没有子类将其重写）
    virtual void speak()
    {
        cout << "小狗在说话" << endl; 
    }


};

// 执行说话的函数
// 地址早绑定  在编译阶段确定函数地址
// 如果想执行让猫说话，那么这个函数地址不能提前绑定，需要在运行阶段进行绑定，地址晚绑定

// 动态多态 满足条件：
//  1.有继承关系
//  2.子类重写父类的虚函数

// ps：重写的概念（补充）
// 重写：函数返回值类型 函数名 参数列表 完全一致称为重写

// 动态多态使用
// 父类的指针或引用 指向子类对象
void doSpeak(Animal &animal) // Animal &animal = cat
{
    animal.speak();
}

void test01()
{
    Cat cat;
    doSpeak(cat);

    Dog dog;
    doSpeak(dog);
}

int main()
{
    test01();

    return 0;
}


// ai的总结
// 相关知识点及注意事项
// 1. 虚函数与动态多态的紧密联系
// 虚函数是实现动态多态的基石，只有将基类中的函数声明为虚函数，派生类对其进行重写才有意义，编译器才能在运行阶段根据对象的实际类型来决定调用哪个类的函数版本。如果遗漏了 virtual 关键字，即便函数名、参数列表、返回值类型等看似符合重写的形式，编译器也只会按照静态绑定的方式处理（也就是在编译阶段就确定调用基类的函数，不会根据对象类型动态切换），无法实现动态多态的效果。
// 2. 函数重写的严格规则
// 正如注释中提到的，函数重写要求函数返回值类型、函数名、参数列表完全一致，这一规则非常严格，稍有偏差就可能不被编译器认定为重写行为，而是当作新定义的函数。不过存在一种特殊情况叫 “协变返回类型”，在满足一定条件下（比如基类虚函数返回基类指针或引用，派生类重写函数返回派生类指针或引用且派生类是基类的子类），返回值类型可以有一定的变化但仍被视为合法的重写，不过在这段简单示例代码中未涉及该特殊情况。另外，重写时函数的访问权限也有要求，派生类重写后的函数访问权限不能比基类中虚函数的访问权限更严格（例如基类虚函数是 public 访问权限，派生类重写函数可以是 public 或 protected，但不能是 private），否则也不符合重写规范，可能导致多态调用出现问题。
// 3. 动态多态应用场景与优势体现
// 动态多态在面向对象编程中应用广泛，尤其适用于具有层次结构的类体系，比如在图形绘制系统中，有基类 Shape（包含虚函数 draw），派生类如 Circle、Rectangle、Triangle 等分别重写 draw 函数来实现各自图形的绘制逻辑，通过一个统一的函数接收 Shape 类指针或引用并调用 draw 函数，就可以根据传入的是不同图形类对象动态地绘制相应图形，这样的设计极大地增强了代码的可扩展性和灵活性。当需要新增一种图形类型时，只需继承 Shape 类并重写 draw 函数即可，无需修改原有的绘制逻辑代码，符合开闭原则（对扩展开放，对修改关闭），方便代码的维护和功能的扩展，这也是动态多态在大型项目中备受青睐的重要原因之一。