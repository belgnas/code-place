//指针-空指针和野指针

//空指针：指针变量指向内存中编号为0的空间
//用途：初始化指针变量
//注意：空指针指向的内存是不可以访问的

//野指针：指针变量指向非法的内存空间（个人理解：就是越界了）

#include<iostream>
using namespace std;

int main()
{
    //空指针
    //1.空指针用于给指针变量进行初始化
    int *p = NULL;

    //2.空指针是不可以访问的
    // 0 ~ 255 之间的内存编号是系统占用的，不允许用户访问
    //错误示范：如下
    *p = 100;
    cout << *p << endl;

    //野指针
    //（！！编译器不会报错）
    //在程序中，尽量避免出现野指针

    //指针变量t指向内存地址编号为0x1100的空间
    int *t = (int *)0x1100;

    //错误示范如下：访问野指针报错
    cout << *t << endl;
    
    system("pause");

    return 0;   
}

