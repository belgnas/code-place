#include <iostream>

using namespace std;

#define int long long

// ========== 全局常量与数组 ==========
const int N = 3e5 + 10;   // 最大节点数（题目约束）
const int MOD = 1e9 + 7;  // 取模用的质数

// 全局数组：避免每次 solve() 都创建 vector，节省内存分配时间
int l_child[N];   // 每个节点的左子节点编号（0 表示不存在）
int r_child[N];   // 每个节点的右子节点编号
int tree_size[N]; // 以该节点为根的子树大小（节点数）
int ans[N];       // 每个节点的最终答案（从该节点出发清理完全部节点的最小总时间）


// ============================================================
// 函数功能：DFS 计算以节点 u 为根的子树大小
// 算法：自底向上，先递归左右子树，再加上自身
// 时间复杂度：O(子树大小)
// ============================================================
void get_size(int u) {
    // u == 0 表示空节点，直接返回
    if (u == 0) return;

    // 自身算 1 个节点
    tree_size[u] = 1;

    // 递归计算左子树大小，加到当前节点
    if (l_child[u] != 0) {
        get_size(l_child[u]);
        tree_size[u] += tree_size[l_child[u]];
    }

    // 递归计算右子树大小，加到当前节点
    if (r_child[u] != 0) {
        get_size(r_child[u]);
        tree_size[u] += tree_size[r_child[u]];
    }
}


// ============================================================
// 函数功能：DFS 计算每个节点的最终答案
// 参数：u = 当前节点编号, parent_ans = 父节点的答案
// 算法：自顶向下，利用父节点答案推导子节点答案
//       公式：ans[u] = parent_ans + (2 * tree_size[u] - 1)
//       解释：先清理完父节点那边的子树，再清理 u 的子树
// 时间复杂度：O(子树大小)
// ============================================================
void get_ans(int u, int parent_ans) {
    if (u == 0) return;

    // ------------------------------------------------------------
    // 计算以节点 u 为起点清理其子树的"额外"时间
    //
    // 深度优先遍历 k 个节点的树：
    //   - 每个节点访问 2 次（进入 + 返回）
    //   - 根节点访问 1 次（最后不返回）
    // 总时间 = 2*k - 1
    //
    // 对 MOD 取模处理负数情况：
    //   - 先取模再减 1 可能变负数
    //   - (+ MOD) % MOD 确保结果在 [0, MOD-1] 范围内
    // ------------------------------------------------------------
    int t_u = (2 * tree_size[u] % MOD - 1 + MOD) % MOD;

    // 当前节点答案 = 父节点答案 + 当前子树清理时间
    ans[u] = (parent_ans + t_u) % MOD;

    // 向下递归，把当前节点的答案作为子节点的 parent_ans
    if (l_child[u] != 0) {
        get_ans(l_child[u], ans[u]);
    }
    if (r_child[u] != 0) {
        get_ans(r_child[u], ans[u]);
    }
}


// ============================================================
// 函数功能：处理单组测试数据
// 输入：n = 节点数, 接下来 n 行每行两个整数表示左右子节点
// 输出：n 个整数，表示每个节点的答案
// ============================================================
void solve() {
    int n;
    cin >> n;

    // 读取每个节点的左右子节点，并初始化相关数组
    for (int i = 1; i <= n; i++) {
        cin >> l_child[i] >> r_child[i];
        tree_size[i] = 0;  // 多组测试数据间需要清零
        ans[i] = 0;
    }

    // 第一遍 DFS：自底向上计算每个子树的大小
    get_size(1);

    // 第二遍 DFS：自顶向下计算每个节点的答案
    // 初始时根节点 1 没有前置清理，所以 parent_ans = 0
    get_ans(1, 0);

    // 输出结果
    for (int i = 1; i <= n; i++) {
        cout << ans[i] << (i == n ? "" : " ");
    }
    cout << "\n";
}


// ============================================================
// 主函数：处理多组测试数据
// ============================================================
signed main() {
    // 关闭同步并解除绑定，大幅提升 cin/cout 速度
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;  // 测试数据组数
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}
