// 数和图是怎么存储的？
// 树是一种特殊的图，无环连通图

// 图分为两种，有向图和无向图（无向图可以看作是特殊的有向图，所以我们只需要了解有向图就可以了）

// 有向图（a -> b）
//  1.邻接矩阵（就是一个2维数组，适合储存稠密图，比较浪费空间）
//  2.邻接链表（）

// 此代码利用邻接表存储图的结构。邻接表是一种常用的图的存储方式，适用于稀疏图（边的数量远小于顶点数量的平方），它可以高效地存储图中顶点之间的连接关系。

#include<iostream>
#include<cstring>
#include<algorithm>

using namespace std;

// 定义常量 N 为图中顶点的最大数量
// 这里假设图最多有 100010 个顶点
const int N = 100010;
// 定义常量 M 为图中边的最大数量
// 由于无向图的每条边会在邻接表中存储两次，所以边的最大数量设为顶点最大数量的 2 倍
const int M = N * 2;

// h 数组是邻接表的头数组，h[i] 存储顶点 i 的邻接表的头指针
// 初始时，所有顶点的邻接表为空，后续添加边时会更新该数组
int h[N];
// e 数组用于存储每条边的终点
// 对于每一条边 (a, b)，e 数组会记录边的终点 b
int e[M];
// ne 数组用于存储下一条边的索引
// 通过 ne 数组可以将同一个顶点的所有邻接边串成一个链表
int ne[M];
// idx 是边的索引计数器，用于记录当前可用的边的编号
// 初始值为 0，每添加一条新边，idx 就会自增 1
int idx;

// 向图中添加一条从顶点 a 到顶点 b 的边的函数
void add(int a, int b) {
    // 将边的终点 b 存储到 e 数组的当前 idx 位置
    e[idx] = b;
    // 将当前边的下一条边的索引设为顶点 a 当前邻接表的头指针
    // 也就是把新边插入到顶点 a 邻接表的头部
    ne[idx] = h[a];
    // 更新顶点 a 的邻接表的头指针为当前边的索引
    h[a] = idx;
    // 边的索引计数器 idx 自增 1，为下一次添加边做准备
    idx++;
}

// 打印图的邻接表的函数，参数 n 表示图中顶点的数量
void printGraph(int n) {
    // 遍历图中的每个顶点
    for (int i = 1; i <= n; i++) {
        // 输出当前顶点的编号
        cout << "顶点 " << i << " 的邻接顶点: ";
        // 从顶点 i 的邻接表的头指针开始遍历其邻接边
        for (int j = h[i]; j != -1; j = ne[j]) {
            // 输出当前边的终点，即顶点 i 的一个邻接顶点
            cout << e[j] << " ";
        }
        // 换行，为下一个顶点的邻接顶点输出做准备
        cout << endl;
    }
}

int main() {
    // 使用 memset 函数将 h 数组的所有元素初始化为 -1
    // -1 表示该顶点的邻接表为空，没有邻接边
    memset(h, -1, sizeof h);

    // 向图中添加边
    // 添加从顶点 1 到顶点 2 的边
    add(1, 2);
    // 添加从顶点 1 到顶点 3 的边
    add(1, 3);
    // 添加从顶点 2 到顶点 3 的边
    add(2, 3);

    // 调用 printGraph 函数打印图的邻接表
    // 这里假设图中有 3 个顶点
    printGraph(3);

    // 程序正常结束，返回 0
    return 0;
}