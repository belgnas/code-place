//指针-指针的定义和使用

//指针变量定义语法：数据类型 * 变量名（！！ps：注意这里是*号！！）

//（ps:&是取地址符！！）
//个人理解：int *p = &a;和int p = &a;的区别
//意思就是*p表示的是某个地址所对应的值，p表示地址这个值本身

#include<iostream>
using namespace std;

int main()
{
    //1.定义指针
    int a = 1;

    //指针变量定义语法：数据类型 *变量名
    int * p;//一般习惯用p来表示指针（point的简写）
    //个人理解：*相当于对p进行了提前声明，声明其是一个指针变量

    //让指针记录变量a的地址
    p = &a;//ps:&是取地址符！！

    cout << "a的地址为：" << &a << endl;
    cout << "指针p为：" << p << endl;
   

    //2.使用指针
    //可以通过解引用的方式来找到指针指向的内存
    //指针前加 * 代表解引用，找到指针指向的内存中的数据
    *p = 1000;
    cout << "a为：" << a << endl;
    cout << "*p为：" << *p << endl;
    //相当于通过指针变量*p直接修改了a所在的内存的值，故a的值自然而然的就被改变了

    
    system("pause");

    return 0;   
}

//找的ai的回答

// 在 C++ 中，“int *p;” 声明了一个指针变量p，它可以存储一个整数类型变量的内存地址。

// 一、*的作用

// 声明指针变量
// 当出现在类型之后变量名之前时，如 “int *p;”，表示声明了一个指向整数类型的指针变量p。这个指针变量可以用来存储一个整数变量的地址。

// 解引用操作
// 当在已声明的指针变量前使用时，如 “*p”，这被称为解引用操作。它可以访问指针所指向的内存地址中的值。例如，如果p存储了变量num的地址，那么 “*p” 就代表变量num的值。可以通过 “*p = value;” 这样的语句来修改指针所指向的内存地址中的值。

// 二、理解方式

// 形象比喻
// 可以把内存想象成一个大旅馆，每个房间都有一个编号（内存地址）。整数变量就像旅馆中的一个房间，里面存放着一个整数值。指针变量就像一个小纸条，上面写着某个房间的编号。当我们声明 “int *p;” 时，就相当于创建了一个小纸条，但此时纸条上还没有写房间编号。
// 当我们执行 “p = #” 时，就相当于把变量num所在房间的编号写在了纸条p上。现在p就指向了变量num。
// 当我们使用 “*p” 时，就相当于根据纸条上的编号找到了对应的房间，并进入房间查看或修改里面的值。

// 实际编程中的意义
// 指针在 C++ 中非常有用，因为它们允许我们直接操作内存地址，实现一些高级的编程技术。例如，通过指针我们可以传递变量的地址给函数，从而在函数内部修改外部变量的值。指针还可以用于动态内存分配，允许程序在运行时根据需要分配和释放内存。
