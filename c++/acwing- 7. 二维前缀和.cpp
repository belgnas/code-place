// 二维前缀和

// S[i, j] = 第i行j列格子左上部分所有元素的和
// 以(x1, y1)为左上角，(x2, y2)为右下角的子矩阵的和为：
// S[x2, y2] - S[x1 - 1, y2] - S[x2, y1 - 1] + S[x1 - 1, y1 - 1]

// 本题题目：
// 输入一个n行m列的整数矩阵，在输入q个询问，每个询问包含4个整数，x1，y1，x2，y2，表示一个子矩阵的左上角坐标和右下角坐标。
// 对于每个询问输出矩阵中所有数的和。

// 二维前缀和是一种高效计算矩阵中子矩阵和的方法，通过预处理前缀和数组，可以在O(1)的时间复杂度内回答任意子矩阵的和。

// **概念**：
// S[i][j] 表示矩阵中从(1, 1)到(i, j)这个矩形区域内的所有元素的和。
// 例如，矩阵中的某个位置(i, j)，其前缀和S[i][j]等于它上方所有行、左边所有列以及左上方所有元素的和。

// **求子矩阵和的公式**：
// 假设我们有一个子矩阵，左上角是(x1, y1)，右下角是(x2, y2)。这个子矩阵的和可以通过前缀和数组计算：
// S[x2][y2] - S[x1 - 1][y2] - S[x2][y1 - 1] + S[x1 - 1][y1 - 1]
// 这个公式的作用是从整个大矩形中减去多余的上下左右部分，只剩下目标子矩阵的和。

// **题目要求**：
// 输入一个整数矩阵，然后处理多个查询。每个查询给出一个子矩阵的左上角和右下角，要求输出该子矩阵的和。

// AcWing 796. 子矩阵的和
// 1、题目（来源于AcWing）：
// 输入一个n行m列的整数矩阵，再输入q个询问，每个询问包含四个整数x1, y1, x2, y2，表示一个子矩阵的左上角坐标和右下角坐标。
// 对于每个询问输出子矩阵中所有数的和。

// 输入格式
// 第一行包含三个整数n，m，q。
// 接下来n行，每行包含m个整数，表示整数矩阵。
// 接下来q行，每行包含四个整数x1, y1, x2, y2，表示一组询问。

// 输出格式
// 共q行，每行输出一个询问的结果。

// 数据范围
// 1 ≤ n, m ≤ 1000,
// 1 ≤ q ≤ 200000,
// 1 ≤ x1 ≤ x2 ≤ n,
// 1 ≤ y1 ≤ y2 ≤ m,
// -1000 ≤ 矩阵内元素的值 ≤ 1000

// 输入样例：
// 3 4 3
// 1 7 2 4
// 3 6 2 8
// 2 1 2 3
// 1 1 2 2
// 2 1 3 4
// 1 3 3 4
// 输出样例：
// 17
// 27
// 21

#include <iostream>
using namespace std;

const int N = 1010; // 定义数组的最大大小为1010x1010（考虑到题目中的输入可能不会超过这个范围）

int n, m, q; // 矩阵的行数、列数，以及查询的次数
int a[N][N]; // 原始矩阵数组
int s[N][N]; // 前缀和数组

int main()
{
    // 读取输入：矩阵的行数、列数和查询次数
    scanf("%d%d%d", &n, &m, &q);

    // 输入矩阵中的元素
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            scanf("%d", &a[i][j]); // 读取矩阵的每个元素，注意数组从1开始索引，方便后续处理边界
        }
    }

    // 计算二维前缀和数组s
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            // 根据二维前缀和的递推公式计算s[i][j]
            s[i][j] = s[i - 1][j] + s[i][j - 1] - s[i - 1][j - 1] + a[i][j];
            // 该公式考虑了相邻行和列的和，减去重复计算的左上角区域
        }
    }

    /*
    // 可选：打印前缀和数组（用于调试）
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            cout << s[i][j] << " ";
        }
        cout << endl;
    }
    */

    // 处理每个查询
    while (q--)
    {
        int x1, y1, x2, y2;
        scanf("%d%d%d%d", &x1, &y1, &x2, &y2); // 读取子矩阵的左上角(x1,y1)和右下角(x2,y2)

        // 根据二维前缀和公式计算子矩阵的和
        int ans = s[x2][y2] - s[x1 - 1][y2] - s[x2][y1 - 1] + s[x1 - 1][y1 - 1];
        printf("%d\n", ans); // 输出结果
    }

    return 0;
}

// **关键点解释**：
// 1. **二维前缀和数组的构建**：
//    通过将当前元素与上方、左方的元素累加，并减去重复计算的左上角区域，得到当前元素的前缀和。
//
// 2. **子矩阵和的计算**：
//    利用容斥原理，从大矩形中减去多余的部分，得到目标子矩阵的和。
//
// 3. **边界处理**：
//    数组从1开始索引，方便处理边界情况（比如x1 -1为0时，对应的区域视为0）。

// 这种方法的时间复杂度为O(nm)预处理，O(1)查询，非常适合处理大量查询的情况。
