// 《碧蓝档案》-砂狼白子

// “白子在哪？”我问。
// “在哪？阿拜多斯？”同学猜。
// “夏莱呗！”朋友不屑。
// “不是在你床上么？”室友打趣。
// 我没理他们，朝外面走去。
// “白子在哪？”我问一只知更鸟。
// “白子吗？我在加州的一棵棕榈树下见过她。黄昏时刻，夕阳漫过天际线撒在了她的裙子上，她整个人都被染成了浪漫的金黄，比丰收时节的小麦还要动人，比草地上盛开的水仙还要唯美，去那找她吧！”
// “白子在哪？”我问一片白雪。
// “白子吗？我在瑞典的极光之间见过她。那晚的极光只宠幸她一人，那夜的雪花也只为她一人而飘落。绚丽多彩的光线比不过她优雅的眼眸，洁白无瑕的冰雪沾不湿她吹弹可破的肌肤，去那找她吧！”
// “白子在哪？”我问一朵浪花。
// “白子吗？我在马尔代夫的沙滩上见过她。她沿着海岸线缓缓漫步，赤脚踩在松软的沙子上，冰冰凉凉的海水一去一来，冲刷着她娇小可爱的脚，海风也识趣的轻轻撩起她的碎花长裙，去那找她吧！”
// “白子在哪？”我问一滴雨水。
// 白子吗？我在江南的弄堂里见过她。细雨微醺，空气氤氲，她撑着一把油纸伞站在巷口，安静地听着雨滴落在青石板上的声音。似是在等人，又仿佛只是单纯的赏雨，去那找她吧！”
// 可我只是我，平凡的不能再平凡，要怎么找到她呢？
// 或许我能在后院采一束夕阳，暖暖的捧在手心，说：“嗨，白子。”
// 或许我能在冰箱拂一抹白霜，细细搓揉直到化掉，说：“嗨，白子。”
// 或许我能在池塘拾到一块圆润的鹅卵石，它依旧带有曾经长眠海底的温度。
// 或许我能在雨季合伞，让杏花般的雨线落在我的脸上，感受吹面不寒的轻柔。
// 原来就在这啊！我恍然大悟，原来白子从来就不在别的地方，白子一直住在我的心里呀！
// 白子，你看！
// 目光所及之处，皆是爱意满怀。

