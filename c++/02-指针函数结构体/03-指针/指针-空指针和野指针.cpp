// 以下是对这段代码的分析：

// 一、整体功能

// 这段 C++ 代码旨在演示空指针和野指针的概念及错误用法。

// 二、代码详解

// 空指针部分：
// int *p = NULL;：声明一个指向整数的指针p并初始化为空指针（在 C++ 中，NULL通常被定义为0或者nullptr，表示指针不指向任何有效的内存地址）。
// *p = 100;和cout << *p << endl;是错误的用法，因为空指针不应该被解引用去访问内存，这样做会导致程序崩溃或出现未定义的行为。

// 野指针部分：
// int *t = (int *)0x1100;：这里将指针t强制转换为指向整数类型并指向内存地址0x1100。这个地址很可能是一个非法的或未分配给程序使用的内存地址，因此t成为了野指针。
// cout << *t << endl;同样是错误的用法，尝试访问野指针指向的内存也会导致程序崩溃或出现未定义的行为。

// 三、总结和注意事项

// 空指针主要用于初始化指针变量，表示指针当前不指向任何有效的内存地址。在使用指针之前，应该确保指针被正确初始化，避免出现未定义的行为。
// 野指针是非常危险的，因为它们可能指向任意的内存地址，可能会导致程序崩溃、数据损坏或安全漏洞。在编程中，应该避免出现野指针，可以通过正确初始化指针、在释放内存后将指针置为nullptr等方式来防止野指针的产生。