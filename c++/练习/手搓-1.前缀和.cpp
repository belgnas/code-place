// 一、题目描述
// 输入一个长度为 n 的整数序列。
// 接下来再输入 m 个询问，每个询问输入一对 l, r。
// 对于每个询问，输出原序列中从第 l 个数到第 r 个数的和。

// 输入格式
// 第一行包含两个整数 n 和 m。
// 第二行包含 n 个整数，表示整数数列。
// 接下来 m 行，每行包含两个整数 l 和 r，表示一个询问的区间范围。

// 输出格式
// 共 m 行，每行输出一个询问的结果。

// 数据范围
// 1 ≤ l ≤ r ≤ n,
// 1 ≤ n, m ≤ 100000,
// -1000 ≤ 数列中元素的值 ≤ 1000

// 输入样例：
// 5 3
// 2 1 3 6 4
// 1 2
// 1 3
// 2 4

// 输出样例：
// 3
// 6
// 10

#include <iostream> // 包含输入输出流库

using namespace std; // 使用标准命名空间，避免频繁使用 std::

const int N = 100010; // 定义前缀和数组的大小，足够容纳较大的输入数据

int s[N], n, m; // 前缀和数组 s，以及输入的数组大小 n 和查询次数 m

int main()
{
    scanf("%d%d", &n, &m); // 读取输入的数组大小 n 和查询次数 m

    // 计算前缀和数组
    s[0] = 0;
    for (int i = 1; i <= n; i++)
    {
        scanf("%d", &s[i]); // 读取数组元素并存入 s[i]
        s[i] += s[i - 1];   // 计算前缀和：s[i] = s[i-1] + a[i]
        // 此处假设输入的数组元素是 a[i]，通过前缀和数组 s 存储累积和
    }

    // 处理 m 次查询
    while (m--)
    {
        int l, r;                        // 定义查询区间的左端点 l 和右端点 r
        scanf("%d%d", &l, &r);           // 读取查询的左右端点
        printf("%d\n", s[r] - s[l - 1]); // 计算区间 [l, r] 的和并输出
        // 前缀和的性质：s[r] - s[l-1] 表示从索引 l 到 r 的和
    }

    return 0; // 程序结束，返回 0 表示正常退出
}