// 题目要求
// 给定一个二维数组（矩阵），找出其中和最大的子矩阵，并返回这个最大和。

// 知识点：二维前缀和，动态规划

#include <iostream>
#include <algorithm>
using namespace std;

const int N = 1010; // 定义数组的最大大小，假设矩阵的行列数不会超过1010

int n, m; // 矩阵的行数和列数
int a[N][N]; // 原始矩阵数组，用于存储输入的矩阵
int s[N][N]; // 前缀和数组，用于存储二维前缀和

int main()
{
    // 输入矩阵的行数和列数
    scanf("%d%d", &n, &m);
    
    // 输入矩阵中的每个元素
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            scanf("%d", &a[i][j]); // 读取矩阵的每个元素，注意数组从 1 开始索引
        }
    }

    // 计算二维前缀和数组s
    for (int i = 1; i <= n; i++) // 注意从 1 开始
    {
        for (int j = 1; j <= m; j++) // 注意从 1 开始
        {
            // 二维前缀和的递推公式：
            // s[i][j] = s[i-1][j] + s[i][j-1] - s[i-1][j-1] + a[i][j]
            // 这个公式的作用是将当前元素与上方、左方的元素累加，同时减去重复计算的左上角区域
            s[i][j] = s[i - 1][j] + s[i][j - 1] - s[i - 1][j - 1] + a[i][j];
        }
    }

    int max_sum = -1e9; // 初始化最大和为一个很小的值，用于存储最大子矩阵和

    // 枚举子矩阵的上下边界
    for (int i = 1; i <= n; i++) // 上边界
    {
        for (int j = i; j <= n; j++) // 下边界
        {
            // 定义一个一维数组dp，用于动态规划求解最大子数组和
            int dp[m + 1];
            dp[0] = 0; // 初始化动态规划数组的第一项为0

            // 遍历每一列，计算每一列的和，并更新动态规划数组
            for (int k = 1; k <= m; k++)
            {
                // 计算第k列从第i行到第j行的和
                int col_sum = s[j][k] - s[i - 1][k] - s[j][k - 1] + s[i - 1][k - 1];

                // 动态规划状态转移方程：
                // dp[k] = max(dp[k-1] + col_sum, col_sum)
                // 这个方程的意思是，当前列的和要么与前一列的和相连，形成一个更大的子数组；要么单独作为一个新的子数组
                dp[k] = max(dp[k - 1] + col_sum, col_sum); // dp[k]指的就是当前起点，当前终点的以第k列结尾的最大子数组和

                // 更新全局最大和
                max_sum = max(max_sum, dp[k]); // 反复比较，找到最大的区间和
            }
        }
    }

    // 输出最大子矩阵和
    printf("%d\n", max_sum);

    return 0;
}

// 代码功能总结
// 这段代码的核心功能是求解一个二维矩阵中的最大子矩阵和。
// 它首先读取矩阵的行数和列数以及矩阵元素，
// 接着计算二维前缀和以便快速获取任意子矩阵的元素和，
// 然后通过枚举子矩阵的上下边界将二维问题转化为一维问题，
// 利用动态规划求解每一种上下边界组合下的最大子数组和，
// 最终得到最大子矩阵和并输出。