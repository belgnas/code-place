// 合并区间（合并所有有交集的区间）

// AcWing 803. 区间合并
// 1、题目（来源于AcWing）：
// 给定 n 个区间 [l,r]，要求合并所有有交集的区间。
// 注意如果在端点处相交，也算有交集。
// 输出合并完成后的区间个数。
// 例如：[1,3]和[2,6]可以合并为一个区间[1,6]。

// 输入格式
// 第一行包含整数n。
// 接下来n行，每行包含两个整数 l 和 r。

// 输出格式
// 共一行，包含一个整数，表示合并区间完成后的区间个数。

// 数据范围
// 1 ≤ n ≤ 100000,
// -10^9 ≤ l ≤ r ≤ 10^9

// 输入样例：
// 5
// 1 2
// 2 4
// 5 6
// 7 8
// 7 9
// 输出样例：
// 3

// 2、基本思想：
// 按区间的左端点排序，当前维护区间的下一个区间有三种情况：
// ①含于当前维护区间
// ②左端点>=当前维护区间左端点但是与当前维护区间有交集
// ③左端点>当前维护区间右端点

// 对于①②两种情况，将当前维护区间更新即可，对于③将当前维护区间换成下一个区间。

// 合并区间（合并所有有交集的区间）

#include <iostream>
#include <algorithm>
#include <vector>
using namespace std;

// 定义一个类型别名 PII，它是一个由两个 int 类型组成的 pair，用于表示区间的左右端点
typedef pair<int, int> PII;

// 定义常量 N，用于表示最大可能的区间数量
const int N = 100010;

// 存储输入的区间数量
int n;
// 存储所有输入的区间，每个区间用一个 PII 表示
vector<PII> segs;

// 合并区间的函数，传入一个存储区间的 vector 的引用，方便直接修改原 vector
void merge(vector<PII> &segs)
{
    // 用于存储合并后的区间
    vector<PII> res;

    // 对输入的区间按照 左端点 进行排序
    // 这样可以保证后续合并操作的顺序性
    sort(segs.begin(), segs.end());

    // 初始化当前合并区间的左端点和右端点为一个极小值
    // 这个极小值用于标记初始状态，确保第一个区间能正确处理
    int st = -2e9, ed = -2e9;

    // 遍历所有输入的区间
    for (auto seg : segs)
    {
        // 如果当前合并区间的右端点小于当前遍历到的区间的左端点
        // 说明这两个区间没有交集
        if (ed < seg.first)
        {
            // 如果 st 不等于初始的极小值，说明已经有一个有效的合并区间
            // 将这个合并区间加入到结果 vector 中
            if (st != -2e9) // 这里用ed判断和st判断是等效的
                res.push_back({st, ed});
            // 更新当前合并区间的左端点为当前遍历到的区间的左端点
            st = seg.first;
            // 更新当前合并区间的右端点为当前遍历到的区间的右端点
            ed = seg.second;
        }
        else
        {
            // 如果两个区间有交集，更新当前合并区间的右端点为两者右端点的最大值
            // 这样可以确保合并后的区间能覆盖所有有交集的部分
            ed = max(ed, seg.second);
        }
    }

    // 处理最后一个合并区间(因为前面最后一步若是执行的是else或第一次执行if（未运行if(st != -2e9)的情况），就没有处理完最后一个合并区间)
    // 如果 st 不等于初始的极小值，说明存在有效的合并区间，将其加入结果 vector
    if (st != -2e9)
        res.push_back({st, ed});

    // 将合并后的区间赋值给原 vector，完成合并操作
    segs = res;
}

int main()
{
    // 从标准输入读取区间的数量
    cin >> n;

    // 循环读取 n 个区间的左右端点
    for (int i = 0; i < n; i++)
    {
        int l, r;
        cin >> l >> r;
        // 将读取到的区间加入到 segs vector 中
        segs.push_back({l, r});
    }

    // 调用 merge 函数对区间进行合并
    merge(segs);

    // 输出合并后区间的数量
    cout << segs.size() << endl;

    return 0;
}